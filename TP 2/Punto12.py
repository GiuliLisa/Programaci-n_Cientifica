#12. Escribí un programa que pregunte al usuario por el número de horas trabajadas y el coste por hora. Después debe mostrar por pantalla la paga que le corresponde.
horas = float(input("Ingrese el número de horas trabajadas: "))
coste = float(input("Ingrese el coste por hora: "))
cobro = horas * coste
print(cobro)