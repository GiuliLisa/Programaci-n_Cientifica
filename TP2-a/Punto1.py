#1. ¿Que aparecerá por pantalla al evaluar la expresión [1] [0] ? ¿Y al evaluar la expresión [] [0]?

#Respuesta: al evaluar [1] [0] muestra 1 porque hay una lista con un elemento y luego el 0 es su indice.
#Al evaluar [] [0] da un error porque la lista está vacía.

print([1][0]) #muestra 1

print([][0]) #da error.